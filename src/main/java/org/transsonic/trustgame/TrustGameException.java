package org.transsonic.trustgame;

public class TrustGameException extends Exception {

    /** */
    private static final long serialVersionUID = 1L;

    public TrustGameException() {
        super();
    }

    public TrustGameException(String message, Throwable cause) {
        super(message, cause);
    }

    public TrustGameException(String message) {
        super(message);
    }

    public TrustGameException(Throwable cause) {
        super(cause);
    }

    
}
